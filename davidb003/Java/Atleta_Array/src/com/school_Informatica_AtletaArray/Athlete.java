package com.school_Informatica_AtletaArray;

public class Athlete {
    String name;
    String team;
    byte goals;

    public Athlete() {
        this.goals = goals;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public void setGoals(byte goals) {
        this.goals = goals;
    }

    public String getName() {
        return name;
    }

    public String getTeam() {
        return team;
    }

    public byte getGoals() {
        return goals;
    }
}
